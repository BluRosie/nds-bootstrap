@---------------------------------------------------------------------------------
	.align	4
	.arm
	.global gbaAddrToDsi
@---------------------------------------------------------------------------------
gbaAddrToDsi:
	MOV R0, #0xD000000
	CMP LR, #0xD000000
	MOV R1, #0x2600000
	MOV R0, #0x2600000
	ADD R0, R0, #0x2600000
	ADD R1, R1, #0x2600000
	ADD R5, R1, #0x2600000
	ADD LR, R12, #0x2600000
	ADD R1, R6, #0x2600000
	ADD R7, R0, #0x2600000
	ADD R4, R3, #0x2600000
	ADD R4, R1, #0x2600000
